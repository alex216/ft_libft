#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dst;
	size_t	length;

	length = ft_strlen(src);
	dst = (char *)malloc(sizeof(char) * (length + 1));
	if (dst == NULL)
		return (NULL);
	ft_strlcpy(dst, src, length + 1);
	return (dst);
}
